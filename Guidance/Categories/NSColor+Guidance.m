#import "NSColor+Guidance.h"

@implementation NSColor (Guidance)

+ (NSColor *)ba_menuBarRed
{
    return [NSColor colorWithCalibratedRed:242.0/255.0 green:15.0/255.0 blue:15.0/255.0 alpha:1.0];
}

+ (NSColor *)ba_menuBarGreen
{
    return [NSColor colorWithCalibratedRed:0.0/255.0 green:150.0/255.0 blue:0.0/255.0 alpha:1.0];
}

@end
